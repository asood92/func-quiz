def area(width, height):
  result = width * height
  return result

def subtract(num1, num2):
  result = num1 - num2
  return result

def add(num1, num2):
  result = num1 + num2
  return result

result = area(5, 6)
print(result)

result = area(10,13)
print(result)

result = area(3,11)
print(result)

result = subtract(4,3)
print(result)

result = subtract(12,3)
print(result)

result = add(7, 12)
print(result)

result = add(33, 6)
print(result)